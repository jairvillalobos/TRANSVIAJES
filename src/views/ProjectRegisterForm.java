    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
            if(this.proyecto.getNomProyecto() == null) {
            
                this.proyecto = new Project(
                    txtnomproyecto.getText(),
                    txtdescripcion.getText(),
                    txtactividades.getText(),
                    txtpresupuesto.getText(),
                    txtfechinicio.getText(),
                    txtfechfinal.getText(),
                    ClientController.getListClient().get(1)
                    );
                
                ProjectController.aggNewProject(this.proyecto);
                dispose();
                ProjectGestion pgt = new ProjectGestion();
                pgt.setVisible(true);

            }else {

                this.proyecto.setDescripcion(txtdescripcion.getText());
                this.proyecto.setActividades(txtactividades.getText());
                this.proyecto.setPresuPuesto(txtpresupuesto.getText());
                this.proyecto.setFechInicio(txtfechinicio.getText());
                this.proyecto.setFechFinal(txtfechfinal.getText());
                ProjectController.updateProject(this.proyecto);
                dispose();
                ProjectGestion pgt = new ProjectGestion();
                pgt.setVisible(true);
                
            }

    }//GEN-LAST:event_jButton1ActionPerformed

